"""Result models produced by backtest runs."""

from __future__ import annotations

from dataclasses import dataclass
from datetime import datetime

from trading_app.execution.orders import Fill, Order, OrderSide
from trading_app.portfolio.models import PortfolioState


@dataclass(frozen=True)
class EquityPoint:
    """One timestamped equity observation in the backtest timeline."""

    timestamp: datetime
    equity: float


@dataclass(frozen=True)
class TradeRecord:
    """Executed trade details captured during the backtest run."""

    timestamp: datetime
    symbol: str
    side: OrderSide
    quantity: float
    price: float
    cash_after: float
    position_after: float


@dataclass(frozen=True)
class BacktestResult:
    """Container for all artifacts generated by a backtest execution."""

    final_state: PortfolioState
    equity_curve: list[EquityPoint]
    orders: list[Order]
    fills: list[Fill]
    trade_log: list[TradeRecord]
    bars_processed: int
